    private void jButtonThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonThemActionPerformed
      
       
        try{
            if(jTextFieldMaPhong.getText().equals("")|| jTextFieldTenPhong.getText().equals("") )
            {
                JOptionPane.showMessageDialog(rootPane," Bạn cần điền đầy đủ dữ kiện");
            }
            else{
                if(ktraTrungMaPhong(jTextFieldMaPhong.getText()))
                {
               
                    phongthi = new PhongThi(jTextFieldMaPhong.getText().toString(), jTextFieldTenPhong.getText().toString());
                    File f = new File("danhSachPhongThi.txt");
                    f.WriteToFile(phongthi.toString());
                    dsPhongThi.add(phongthi);
                    LoadTablePhongThi();
                   }
                else{
                    JOptionPane.showMessageDialog(rootPane, "Mã phòng này đã tồn tại");
                }
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_jButtonThemActionPerformed

