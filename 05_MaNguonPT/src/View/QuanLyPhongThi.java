    void LoadTableFromFile(){
        File f = new File("danhSachPhongThi.txt");
        ArrayList<String> list = f.ReadFromFile();
        for(String s : list){
            String[] txt = s.split(",");
            phongthi = new PhongThi(txt[0],txt[1]);
            dsPhongThi.add(phongthi);
        }
    }
    
    private void WriteTableToFile(){
        try {
                FileWriter fr = new FileWriter("danhSachPhongThi.txt");
                for(PhongThi p : dsPhongThi){
                    fr.write(p.getMaPhong() + "," + p.getTenPhong() + "\n");
                }
                fr.close();
            } catch (IOException ex) {
                Logger.getLogger(QuanLyMonThi.class.getName()).log(Level.SEVERE, null, ex);
            }
    }
    boolean ktraTrungMaPhong(String a)
    {
        boolean ok = true;
        for(PhongThi i:dsPhongThi)
        {
            if(i.getMaPhong().equalsIgnoreCase(a))
                ok = false;
        }
        return ok;
    }
    
    public void LoadTablePhongThi(){
       jTableQuanLyPhongThi.setModel(new TablePhongThi(dsPhongThi));
    }

