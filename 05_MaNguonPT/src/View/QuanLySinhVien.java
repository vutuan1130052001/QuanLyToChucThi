    void LoadTableFromFile(){
        File f = new File("danhSachSinhVien.txt");
        File f1 = new File("danhSachKhoa.txt");
        ArrayList<String> list = f.ReadFromFile();
        ArrayList<String> listkhoa = f1.ReadFromFile();
        for(String s : list){
            String[] txt = s.split(",");
            sinhvien = new SinhVien(txt[0],txt[1],txt[2],txt[3],txt[4],txt[5],txt[6]);
            dsSinhVien.add(sinhvien);
        }
        for(String s : listkhoa){
            String[] txt = s.split(",");
            khoa = new Khoa(txt[0],txt[1]);
            dsKhoa.add(khoa);
        }
    }
    
    private void WriteTableToFile(){
        try {
                FileWriter fr = new FileWriter("danhSachSinhVien.txt");
                for(SinhVien sv : dsSinhVien){
                    fr.write(sv.toString() + "\n");
                }
                fr.close();
            } catch (IOException ex) {
                Logger.getLogger(QuanLyMonThi.class.getName()).log(Level.SEVERE, null, ex);
            }
    }
    
