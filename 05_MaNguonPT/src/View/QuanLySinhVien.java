    boolean ktraTrungMaSv(String a)
    {
        boolean ok = true;
        for(SinhVien i:dsSinhVien)
        {
            if(i.getMaSV().equalsIgnoreCase(a))
                ok = false;
        }
        return ok;
    }
    private void jButtonThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonThemActionPerformed
        // TODO add your handling code here:
         try{
            if(jTextFieldMaSV.getText().equals("")|| jTextFieldTenSV.getText().equals("") || jTextFieldQueQuan.getText().equals("")||
                   jTextFieldNgaySinh.getText().equals("")||jTextSDT.getText().equals(""))
            {
                JOptionPane.showMessageDialog(rootPane," Bạn cần điền đầy đủ dữ kiện");
            }
            else{
                if(ktraTrungMaSv(jTextFieldMaSV.getText()))
                {
                    String dk = jComboBoxDieuKienThi.getSelectedItem().toString();
                    String khoa = cbkhoa.getSelectedItem().toString();
                    sinhvien = new SinhVien(jTextFieldMaSV.getText().toString(), jTextFieldTenSV.getText().toString(),jTextFieldQueQuan.getText().toString() ,jTextFieldNgaySinh.getText().toString(),jTextSDT.getText().toString(),khoa, dk);
                    File f = new File("danhSachSinhVien.txt");
                    f.WriteToFile(sinhvien.toString());
                    dsSinhVien.add(sinhvien);
                    LoadTableSinhVien();
                   }
                else{
                    JOptionPane.showMessageDialog(rootPane, "Mã sinh viên này đã tồn tại");
                }
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_jButtonThemActionPerformed

    private void jButtonSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSuaActionPerformed
        // TODO add your handling code here:
        dongchon = jTableQuanLySinhVien.getSelectedRow();
        if(dongchon !=-1){
            try {
            if(jTextFieldMaSV.getText().equals("")|| jTextFieldTenSV.getText().equals("") || jTextFieldQueQuan.getText().equals("")||
                   jTextFieldNgaySinh.getText().equals("")||jTextSDT.getText().equals(""))
            {
                JOptionPane.showMessageDialog(rootPane," Bạn cần điền đầy đủ dữ kiện");
            }
            else{
                
                    String dk = jComboBoxDieuKienThi.getSelectedItem().toString();
                    String khoa = cbkhoa.getSelectedItem().toString();
                        SinhVien sinhvienNew = new SinhVien(jTextFieldMaSV.getText().toString(), jTextFieldTenSV.getText().toString(),jTextFieldQueQuan.getText().toString() ,jTextFieldNgaySinh.getText().toString(),jTextSDT.getText().toString(),khoa, dk);
                        dsSinhVien.set(dongchon,sinhvienNew);
                        WriteTableToFile();
                        LoadTableSinhVien();
                 
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
        }else{
            JOptionPane.showConfirmDialog(this,"Bạn chưa chọn dòng để sửa!","Thông báo", WIDTH);
        }
    }//GEN-LAST:event_jButtonSuaActionPerformed

    private void jButtonXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonXoaActionPerformed
        // TODO add your handling code here:
        dongchon = jTableQuanLySinhVien.getSelectedRow();
        if(dongchon !=-1){
            dsSinhVien.remove(dongchon);
            WriteTableToFile();
            LoadTableSinhVien();
        }else{
            JOptionPane.showConfirmDialog(this,"Bạn chưa chọn dòng để xóa!","Thông báo", WIDTH);
        }
    }//GEN-LAST:event_jButtonXoaActionPerformed

    private void jTableQuanLySinhVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableQuanLySinhVienMouseClicked
        // TODO add your handling code here:
        dongchon = jTableQuanLySinhVien.getSelectedRow();
        if(dongchon !=-1){
            sinhvien = dsSinhVien.get(dongchon);
            jTextFieldMaSV.setText(sinhvien.getMaSV()+"");
            jTextFieldTenSV.setText(sinhvien.getTenSV()+"");
            jTextFieldQueQuan.setText(sinhvien.getQueQuan()+"");
            jTextFieldNgaySinh.setText(sinhvien.getNgaySinh()+"");
            jTextSDT.setText(sinhvien.getSoDienThoai()+"");
//            Khoa.setText(sinhvien.getKhoa()+"");
            jComboBoxDieuKienThi.setSelectedItem(sinhvien.getDieuKienThi()+"");
        } 
    }//GEN-LAST:event_jTableQuanLySinhVienMouseClicked

