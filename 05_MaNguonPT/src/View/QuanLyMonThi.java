    void indexComboBox(){
        cbPhongThi.removeAllItems();
        for(PhongThi i :dsPhongThi){
            cbPhongThi.addItem(i.getTenPhong());
        }
        cbKhoa.removeAllItems();
        for(Khoa i :dsKhoa){
            cbKhoa.addItem(i.getTenKhoa());
        }
        cbKy.removeAllItems();
        for(Ky i :dsKy){
            cbKy.addItem(i.getTenKy());
        }
        cbDot.removeAllItems();
        for(Dot i :dsDot){
            cbDot.addItem(i.getTenDot());
        }
    }
