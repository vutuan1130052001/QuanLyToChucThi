    void LoadTableFromFile(){
        File f = new File("danhSachMonThi.txt");
        File f1 = new File("danhSachKhoa.txt");
        File f2 = new File("danhSachKy.txt");
        File f3 = new File("danhSachDot.txt");
        File f4=new File("danhSachPhongThi.txt");
        ArrayList<String> list = f.ReadFromFile();
        ArrayList<String> listkhoa = f1.ReadFromFile();
        ArrayList<String> listky = f2.ReadFromFile();
        ArrayList<String> listdot = f3.ReadFromFile();
        ArrayList<String> listphong = f4.ReadFromFile();
        for(String s : list){
            String[] txt = s.split(",");
            monthi = new MonThi(txt[0],txt[1],txt[2],txt[3],txt[4],txt[5],txt[6]);
            dsMonThi.add(monthi);
        }
        for(String s : listkhoa){
            String[] txt = s.split(",");
            khoa = new Khoa(txt[0],txt[1]);
            dsKhoa.add(khoa);
        }
        for(String s : listky){
            String[] txt = s.split(",");
            ky = new Ky(txt[0],txt[1]);
            dsKy.add(ky);
        }
        for(String s : listdot){
            String[] txt = s.split(",");
            dot = new Dot(txt[0],txt[1]);
            dsDot.add(dot);
        }
        for(String s : listphong){
            String[] txt = s.split(",");
            phong = new PhongThi(txt[0],txt[1]);
            dsPhongThi.add(phong);
        }
    }
    
